import type { DeltaTime } from "./types.js";
import type { World } from "./world.js";

/**
 * システムを表すインターフェースのドキュメントコメント。
 *
 * ワールド（World）の状態を進めるためのロジックを実装します。
 * ゲームループやエンティティ・コンポーネント・システムの更新フェーズで呼び出されることを想定しています。
 *
 * @remarks
 * 実装は主に副作用として World を変更します。update は短時間で完了することを想定しており、ブロッキング処理や重い計算は別スレッドや分割処理で行うことを推奨します。
 *
 * @param world - 更新対象の World オブジェクト。エンティティやコンポーネントの取得・変更を行います。
 * @param deltaTime - 前回の更新からの経過時間（フレーム間隔）。単位は実装に依存しますが、一般には秒単位で扱われます。
 *
 * @returns なし（副作用として World を更新します）。
 */
export interface System {
  /**
   * システムを表すインターフェース。
   *
   * ワールドの状態を進める役割を持ち、各フレームまたはタイムステップごとに呼び出されて処理を行います。
   *
   * @remarks
   * 実装はワールド内のエンティティやコンポーネントに対して副作用（追加・削除・更新など）を行うことが想定されます。
   * システムは可能な限り軽量に保ち、deltaTime を用いてフレームレートに依存しない挙動を実現してください。
   *
   * @param world - 更新対象のワールドオブジェクト。
   * @param deltaTime - 経過時間（タイムステップ）。通常は秒単位やミリ秒単位で渡され、時間に基づく計算に使用します。
   */
  update(world: World, deltaTime: DeltaTime): void;
}
